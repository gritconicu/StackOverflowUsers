
public class StackOverflowTag {
	private boolean has_synonyms;
	private int user_id;
	private boolean is_moderator_only;
	private boolean is_required;
	private int count;
	private String name;

	public StackOverflowTag() {
	}

	public boolean isHas_synonyms() {
		return has_synonyms;
	}

	public int getUser_id() {
		return user_id;
	}

	public boolean isIs_moderator_only() {
		return is_moderator_only;
	}

	public boolean isIs_required() {
		return is_required;
	}

	public String getName() {
		return name;
	}

	public int getCount() {
		return count;
	}
}
